package com.example.taller;


public class isEmpty {

    public static void main(String[] args) {
        String texto1 = "Hola";
        String texto2 = "";

        // Verificar si el texto1 está vacío
        if (texto1.isEmpty()) {
            System.out.println("El texto1 está vacío");
        } else {
            System.out.println("El texto1 no está vacío");
        }

        // Verificar si el texto2 está vacío
        if (texto2.isEmpty()) {
            System.out.println("El texto2 está vacío");
        } else {
            System.out.println("El texto2 no está vacío");
        }
    }
}



